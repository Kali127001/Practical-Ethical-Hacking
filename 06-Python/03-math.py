#!/bin/python3

print(50 + 50); # add
print(50 - 50); # subtract
print(50 * 50); # multiply
print(50 / 50); # divide

print(50 + 50 - 50 * 50 / 50); # PEDMAS => Parenthesis, Exponent, Divide, Multiply, Add, Subtract

print(50 ** 2) # exponents

print(50 % 6) # remainder

print(50 // 6); # no remainder